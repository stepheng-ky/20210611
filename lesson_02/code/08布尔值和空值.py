# 布尔值 bool
# 布尔值主要用来做逻辑判断
# 布尔值有两个值： True 真、False 假 首字母大写
a = True
print(f'a = {a}')
b = False
print(f'b = {b}')

# 布尔值实际也属于整形 True相当于1，False相当于0
print(1 + True)
print(1 + False)

# None 空值 专门用来表示不存在
b = None
print(b)

