# 功能描述  ： + - * /
a = 10 + 5
print(a)
a = 10 - 5
print(a)
a = a - 2
print(a)
a = 10 * 5
print(a)
a = 10 / 5
print(a) # 除法总会返回浮点数
# a = 10 / 0 # 报错
print(a)
print(10 / 3)
# // 整除 除完之后只保留整数部分
print(10 // 3)
print(20 // 3)
# ** 求幂
print(2 ** 2)
print(2 ** 5)
print(16 ** 0.5)
# % 求余
print(10 % 2)
print(10 % 4)
